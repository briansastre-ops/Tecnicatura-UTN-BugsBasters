

edad = int(input("Introduzca su edad: "))

if 0 <= edad < 10:
    print("La infancia es increible y bella")
elif 10 <= edad < 20:
    print("Tienes muchos cambios, mucho que estudiar")
elif 20 <= edad < 29:
    print("Amor y comienza el trabajo")
elif 29 <= edad < 39:
    print("consolidación personal, busca el equilibrio")
elif 39 <= edad < 49:
    print("Los recuerdos se vuelven más valiosos que las metas")
elif 49 <= edad < 59:
    print("Días nostalgicos, buenos para repasar tu camino y ver hasta donde has llegado")
elif 59 <= edad < 69:
    print("Cosechas lo que siembras, ahora a disfrutar del tiempo y de la familia")
elif 69 <= edad < 79:
    print("Tranquilidad y legado, aprovecha para enseñarle a los más jovenes eso que solo tu sabes")
elif edad > 79:
    print("Tesoro de experiencias, eres historia viva.")